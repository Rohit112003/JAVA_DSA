package ust.oops;

public class Student_Details {
    String studentId, name, mobile, email, deptCode, teacherId;

    Student_Details(String studentId, String name, String mobile, String email, String deptCode, String teacherId) {
        this.studentId = studentId;
        this.name = name;
        this.mobile = mobile;
        this.email = email;
        this.deptCode = deptCode;
        this.teacherId = teacherId;
    }

}
