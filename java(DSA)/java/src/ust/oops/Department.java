package ust.oops;

public class Department {
    String deptCode, deptName, building;
    int yearFounded;

    Department(String deptCode, String deptName, String building, int yearFounded) {
        this.deptCode = deptCode;
        this.deptName = deptName;
        this.building = building;
        this.yearFounded = yearFounded;
    }
}
